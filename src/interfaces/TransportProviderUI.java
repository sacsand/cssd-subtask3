    public void packagesTableLoad(Vector<Packages> setOfPackages ){
        col_names = new Vector();
        col_names.add("Package ID");
        col_names.add("Package Name");
        col_names.add("Peak Charge");
        col_names.add("Off Peak Charge");
        col_names.add("Amount needed");
     
        packagesForTable = new Vector<Vector<String>>();
      for(Packages pack:setOfPackages)
          {
            Vector v=new Vector();
            v.add(pack.getPackageId());
            v.add(pack.getPackageName());
            v.add(pack.getPeakCharge());
            v.add(pack.getOffPeakCharge());
            v.add(pack.getAmountNeeded());
       
            
         packagesForTable.add(v);
           
          }
        model = new DefaultTableModel( packagesForTable,col_names);
        jTable_packages.setModel(model);
        sort_package= new TableRowSorter<TableModel> (model);
        jTable_packages.setRowSorter( sort_package);
        jScrollPane1.setViewportView( jTable_packages);
        jTable_packages.setAutoscrolls(true);
        jTable_packages.getTableHeader().setReorderingAllowed(false);
    
    
    }
    

