    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
          //get details from the textfields 
           if(jTextField_packageName.getText().equalsIgnoreCase("")||jTextField_peakCharge.getText().equalsIgnoreCase("") || jTextField_offPeakCharge.getText().equalsIgnoreCase("") || jTextField_amountNeeded.getText().equalsIgnoreCase("")){
                JOptionPane.showMessageDialog(rootPane, "Please fill all the necessary fields");
           }
           else{
            String packageName  = jTextField_packageName.getText();
            double peakCharge    = Double.parseDouble(jTextField_peakCharge.getText());
            double offPeakCharge = Double.parseDouble(jTextField_offPeakCharge.getText());
            double amountNeeded = Double.parseDouble(jTextField_amountNeeded.getText());
            
         //creating a new package 
            Packages pack = new Packages(packageName,peakCharge,offPeakCharge,amountNeeded);
            
             PackageControl.getInstance().createPackage(pack);
             
               try {
                   //serializing the packages
                   PackageControl.serialize();
                   JOptionPane.showMessageDialog(rootPane, "Package successfully added");
                   jTextField_packageName.setText("");
                   jTextField_peakCharge.setText("");
                   jTextField_offPeakCharge.setText("");
                   jTextField_amountNeeded.setText("");
                 
                   
               } catch (IOException e) {
                   
                   JOptionPane.showMessageDialog(rootPane, "An error ocuured while adding the package.please reenter");
               }
            }
               //loading packages
                PackageControl packageControlInstance=PackageControl.getInstance();
                Vector<Packages> allPackages=  packageControlInstance.getAllPackages();
                packagesTableLoad(allPackages);
            
          
    }//GEN-LAST:event_jButton1ActionPerformed

